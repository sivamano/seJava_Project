// COMPLILE-TIIME POLYMORPHISIM -> METHOD OVERLOADING
// OVERLOADING BY CHANGING TYPE OF ARGUMENTS
public class Arithm {
    int multiply(int a, int b)
    {
        int c = a*b;
        return c;
    }

    double multiply(double a, double b)
    {
        double c = a*b;
        return c;
    }
}

